public class StringExercise {
    public static void main(String [] args){
        String education = "We don't need no education";
        System.out.println(education);
        String newEducation = education.replace("education", "thought control");
        System.out.println(newEducation);
        System.out.println();

        String look = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(look);
        System.out.println();

        String sentence = "In windows, the main drive is usually C:\\";
        System.out.println(sentence);
        System.out.println();

        String backslashes = "I can do backslashes \\, double backslashes \\\\, \n and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);

    }
}
